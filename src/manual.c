#include "manual.h"

const char *manual_text(void) {
  return "Garmin MTP directory manual\n"
         "\n"
         "Paths vary by watch model, firmware, map set, and enabled features. Run `garmin-mtp list /` or\n"
         "`garmin-mtp dump` first, then use the visible path in `pull` or `push` commands. Some MTP\n"
         "providers show a storage prefix such as `/Internal Storage`; when in doubt pass\n"
         "`--storage \"Internal Storage\"`.\n"
         "\n"
         "Common paths from Garmin watches\n"
         "\n"
         "  /RunningTrack\n"
         "    Small FIT snippets related to running track calibration/recognition. Usually quick to pull.\n"
         "    Example: garmin-mtp pull /RunningTrack/F72J1241.fit ./F72J1241.fit\n"
         "\n"
         "  /Records\n"
         "    Activity records, history, and generated FIT-like data. This can contain many files; listing\n"
         "    or pulling a whole set one by one may be slow.\n"
         "\n"
         "  /Metrics\n"
         "    Health, training, and sensor metric data. Files may be numerous and can take time to enumerate.\n"
         "\n"
         "  /Monitor\n"
         "    Background monitoring data such as wellness/physiology samples. Treat as personal data and\n"
         "    expect large pulls to be slow.\n"
         "\n"
         "  /Location\n"
         "    Location-related cache or position data. Contents depend heavily on enabled features.\n"
         "\n"
         "  /MltSport\n"
         "    Multisport/session data. Useful when investigating activity exports or sport-mode records.\n"
         "\n"
         "  /NewFiles\n"
         "    Import/drop folder used by Garmin devices for supported incoming files. Use `push` carefully\n"
         "    and only with file types the watch expects.\n"
         "\n"
         "  /RemoteSW\n"
         "    Software/update-related files. Avoid writing here unless you know the firmware workflow.\n"
         "\n"
         "  /PaceBands\n"
         "    Pace band data. Usually small.\n"
         "\n"
         "  /PowerGuide\n"
         "    Power guide and workout pacing data. Usually small to moderate.\n"
         "\n"
         "  /PointCache\n"
         "    Cached points/route/navigation data. Can grow depending on maps and navigation usage.\n"
         "\n"
         "  /JetLag\n"
         "    Jet lag adviser data. Usually small.\n"
         "\n"
         "  /inReach\n"
         "    inReach-related data on supported devices/accounts. Treat as personal communications/location\n"
         "    data.\n"
         "\n"
         "  /http\n"
         "    HTTP/network cache or transfer state used by Garmin software. Usually not a first choice for\n"
         "    manual pulls.\n"
         "\n"
         "  /*.img, for example /otm-china.img\n"
         "    Map image files. These can be multiple gigabytes; pulling them over MTP may take a very long\n"
         "    time and can make the watch feel busy until the transfer finishes.\n"
         "\n"
         "Pulling guidance\n"
         "\n"
         "  1. Start with `garmin-mtp list /` to find top-level folders.\n"
         "  2. Use `garmin-mtp manual` or `garmin-mtp menu` to choose a likely folder.\n"
         "  3. List the specific folder before pulling: `garmin-mtp list /RunningTrack`.\n"
         "  4. Pull individual files instead of whole large folders.\n"
         "  5. For map files, Records, Metrics, Monitor, and large caches, expect transfers to be slow.\n";
}

void print_manual(FILE *out) {
  fputs(manual_text(), out);
}
